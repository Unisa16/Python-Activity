# Task 1: Arithmetic Operations using two variable numbers for int, float, and complex
# Integer arithmetic
int_x = 10
int_y = 3
print("\nTask 1 Output:")
print("Integer Operations:")
print(f"Addition: {int_x + int_y}")
print(f"Subtraction: {int_x - int_y}")
print(f"Multiplication: {int_x * int_y}")
print(f"Division: {int_x / int_y}")
print(f"Modulus: {int_x % int_y}")
print(f"Exponent: {int_x ** int_y}")

# Float arithmetic
float_num1 = 10.5
float_num2 = 2.5
print("\nFloat Operations:")
print(f"Addition: {float_num1 + float_num2}")
print(f"Subtraction: {float_num1 - float_num2}")
print(f"Multiplication: {float_num1 * float_num2}")
print(f"Division: {float_num1 / float_num2}")
print(f"Modulus: {float_num1 % float_num2}")
print(f"Exponent: {float_num1 ** float_num2}")

# Complex arithmetic
complex_num1 = 2 + 3
complex_num2 = 1 + 4
print("\nComplex Operations:")
print(f"Addition: {complex_num1 + complex_num2}")
print(f"Subtraction: {complex_num1 - complex_num2}")
print(f"Multiplication: {complex_num1 * complex_num2}")
print(f"Division: {complex_num1 / complex_num2}")

